/* 
Парне чи непарне число
Напишіть програму, яка визначає, чи число парне або непарне.  

Вхід: Число (наприклад, 4)
Вихід:
"Число парне."
"Число непарне." */

export function isOddOrEven(numberValue: number): string {
  if (numberValue % 2 === 0) {
    return "Число парне.";
  } else {
    return "Число непарне.";
  }
}
